    checks_compile(chk, "HAVE_CLOCK_GETTIME_MONOTONIC",
        "#include <time.h>\nint main(void) { struct timespec ts; clock_gettime(CLOCK_MONOTONIC, &ts); return 0; }");
    checks_compile(chk, "HAVE_ATOMIC",
        "#include <stdatomic.h>\nint main(void) { _Atomic int x = 0; (void)x; return 0; }");
    checks_compile(chk, "HAVE_VARIADIC_MACROS_C99",
        "#define TEST(x, ...) x\nint main(void) { TEST(0, 1, 2); return 0; }");
    checks_compile(chk, "HAVE_WRITABLE_ARGV",
        "int main(int argc, char **argv) { argv[0][0] = ' '; return 0; }");
