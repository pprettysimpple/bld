    checks_func(chk, "HAVE_GETPPID",          "getppid",          "unistd.h");
    checks_func(chk, "HAVE_SNPRINTF",         "snprintf",         "stdio.h");
    checks_func(chk, "HAVE_STRDUP",           "strdup",           "string.h");
    checks_func(chk, "HAVE_STRTOK_R",         "strtok_r",         "string.h");
    checks_func(chk, "HAVE_STRTOLL",          "strtoll",          "stdlib.h");

    /* --- Compile checks (complex feature detection) --- */
    checks_compile(chk, "HAVE_POSIX_STRERROR_R",
        "#include <string.h>\nint main(void) { char buf[100]; int r = strerror_r(0, buf, sizeof(buf)); (void)r; return 0; }");
    checks_compile(chk, "HAVE_GLIBC_STRERROR_R",
        "#include <string.h>\nint main(void) { char buf[100]; char *r = strerror_r(0, buf, sizeof(buf)); (void)r; return 0; }");
    checks_compile(chk, "HAVE_GETHOSTBYNAME_R_6",
        "#include <netdb.h>\nint main(void) { struct hostent h, *hp; char buf[1024]; int err;\n  gethostbyname_r(\"\", &h, buf, sizeof(buf), &hp, &err); return 0; }");
    checks_compile(chk, "HAVE_GETHOSTBYNAME_R_5",
        "#include <netdb.h>\nint main(void) { struct hostent h; char buf[1024]; int err;\n  gethostbyname_r(\"\", &h, buf, sizeof(buf), &err); return 0; }");
    checks_compile(chk, "HAVE_GETHOSTBYNAME_R_3",
        "#include <netdb.h>\nint main(void) { struct hostent_data data; struct hostent h;\n  gethostbyname_r(\"\", &h, &data); return 0; }");
    checks_compile(chk, "HAVE_FSETXATTR_5",
        "#include <sys/xattr.h>\nint main(void) { fsetxattr(0, \"\", \"\", 0, 0); return 0; }");
